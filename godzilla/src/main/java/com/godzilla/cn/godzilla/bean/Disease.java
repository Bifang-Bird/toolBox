package com.godzilla.cn.godzilla.bean;

public class Disease {

    private long kid;
    private String id;
    private String dname;
    private String symName;
    private String esymName;
    private String dtype;
    private String ditype;
    private String pid;


    public long getKid() {
        return kid;
    }

    public void setKid(long kid) {
        this.kid = kid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDname() {
        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    public String getSymName() {
        return symName;
    }

    public void setSymName(String symName) {
        this.symName = symName;
    }

    public String getEsymName() {
        return esymName;
    }

    public void setEsymName(String esymName) {
        this.esymName = esymName;
    }

    public String getDtype() {
        return dtype;
    }

    public void setDtype(String dtype) {
        this.dtype = dtype;
    }

    public String getDitype() {
        return ditype;
    }

    public void setDitype(String ditype) {
        this.ditype = ditype;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }
}

